package com.nikolas.codewars.kyu8;

/**
 * ════════════════════════════════════════════════════════════
 *  🎯 Codewars: Opposites Attract
 * ════════════════════════════════════════════════════════════
 *
 *  📋 Задача:
 *     Если у одного цветка чётное число лепестков, а у другого
 *     нечётное — они влюблены (true). Иначе — false.
 *
 *  📊 Примеры:
 *     1, 4  →  true   (нечёт + чёт)
 *     2, 2  →  false  (чёт + чёт)
 *     1, 1  →  false  (нечёт + нечёт)
 *     0, 1  →  true   (чёт + нечёт)
 *
 *  ⚡ Решение:
 *     Сравнение остатков от деления на 2.
 *     Разные остатки = разная чётность = влюблены.
 *
 *  📈 Сложность: O(1) время | O(1) память
 *
 *  🔗 https://www.codewars.com/kata/555086d53eac039a2a000083
 *
 */
public final class OppositesAttract {

    private OppositesAttract() {
        throw new UnsupportedOperationException("Утилитарный класс");
    }

    /**
     * Проверяет, что чётности двух чисел различаются.
     *
     *  Почему сравнение остатков:
     *     — "Остатки не равны" — читается как описание задачи
     *     — (f1 ^ f2) & 1 — быстрее, но менее читаемо
     *     — Длинный if/else с 4 условиями — многословно
     *     — Обе операции % и != — по одной инструкции CPU
     *
     * @param flower1 число лепестков первого цветка (>= 0)
     * @param flower2 число лепестков второго цветка (>= 0)
     * @return true если чётности различаются
     */
    public static boolean isLove(final int flower1, final int flower2) {
        // Разные остатки = разная чётность = влюблены
        return (flower1 % 2) != (flower2 % 2);
    }
}