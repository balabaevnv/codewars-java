package com.nikolas.codewars.kyu8;

/**
 * ════════════════════════════════════════════════════════════
 *  🎯 Codewars: Jenny's Secret Message
 * ════════════════════════════════════════════════════════════
 *
 *  📋 Задача:
 *     Нужно исправить ошибку в функции, которая должна возвращать стандартное приветствие,
 *     но для "Johnny" возвращать специальное приветствие.
 *
 *  📊 Примеры:
 *     "Jim"    → "Hello, Jim!"
 *     "Johnny" → "Hello, my love!"
 *     "Jane"   → "Hello, Jane!"
 *     "Simon"  → "Hello, Simon!"
 *
 *  ⚡ Решение:
 *     Мы исправим ошибку в коде, переставив условие с "Johnny" на правильное место.
 *     Для "Johnny" всегда возвращаем "Hello, my love!", для остальных имён — стандартное приветствие.
 *
 *  📈 Сложность: O(1) время | O(1) память
 *     Время и память затрачиваются минимально, так как мы просто проверяем одно условие.
 *
 *  🔗 https://www.codewars.com/kata/55225023e1be1ec8bc000390/train/java
 *
 */
public final class JennysSecretMessage {

    private JennysSecretMessage() {
        throw new UnsupportedOperationException("Утилитарный класс");
    }

    /**
     * Возвращает приветствие в зависимости от имени.
     *
     *  Почему так:
     *     — Мы проверяем имя на совпадение с "Johnny" и в случае совпадения
     *       возвращаем особое приветствие. В остальных случаях используем стандартное.
     *     — Для всех имен результат генерируется за одну операцию.
     *
     * @param name имя
     * @return соответствующее приветствие
     */
    public static String greet(final String name) {
        // Если имя "Johnny", возвращаем особое приветствие
        if ("Johnny".equals(name)) {
            return "Hello, my love!";
        }
        // Для всех остальных имен стандартное приветствие
        return String.format("Hello, %s!", name);
    }
}