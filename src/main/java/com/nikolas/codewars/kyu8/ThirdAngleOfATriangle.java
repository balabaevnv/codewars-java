package com.nikolas.codewars.kyu8;

/**
 * ════════════════════════════════════════════════════════════
 *  🎯 Codewars: Third Angle of a Triangle
 * ════════════════════════════════════════════════════════════
 *
 *  📋 Задача:
 *     Вам даны два угла треугольника, необходимо найти третий угол.
 *
 *  📊 Примеры:
 *     angle1 = 45, angle2 = 12 → третий угол = 123
 *     angle1 = 50, angle2 = 60 → третий угол = 70
 *
 *  ⚡ Решение:
 *     Сумма углов треугольника всегда равна 180°. Мы просто вычитаем два известных угла из 180.
 *     Простое и эффективное решение.
 *
 *  📈 Сложность: O(1) время | O(1) память
 *     Мы используем только несколько операций, без циклов или других дополнительных структур данных.
 *
 *  🔗 https://www.codewars.com/kata/5a023c426975981341000014/train/java
 *
 */
public final class ThirdAngleOfATriangle {

    private ThirdAngleOfATriangle() {
        throw new UnsupportedOperationException("Утилитарный класс");
    }

    /**
     * Вычисляет третий угол треугольника.
     * Сумма всех углов треугольника всегда 180°.
     *
     * @param angle1 Первый угол.
     * @param angle2 Второй угол.
     * @return Третий угол.
     */
    public static int otherAngle(int angle1, int angle2) {
        // Третий угол = 180 - сумма двух других углов
        return 180 - (angle1 + angle2);
    }
}