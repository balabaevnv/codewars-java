package com.nikolas.codewars.kyu8;

/**
 * ════════════════════════════════════════════════════════════
 *  🎯 Codewars: Fake Binary
 * ════════════════════════════════════════════════════════════
 *
 *  📋 Задача:
 *     Заменить каждую цифру строки:
 *       < 5  → '0'
 *       ≥ 5  → '1'
 *
 *  📌 Гарантия:
 *     Входная строка не пустая и содержит только цифры.
 *
 *  ⚡ Решение:
 *     Работаем напрямую с char[].
 *
 *  📈 Сложность: O(n) время | O(n) память
 *
 */
public final class FakeBinary {

    private FakeBinary() {
        throw new UnsupportedOperationException("Утилитарный класс");
    }

    /**
     * Преобразует строку цифр в бинарную строку по правилу.
     *
     *  Почему именно так:
     *   — 1 проход
     *   — без Regex (очень дорого)
     *   — без Stream (лишние аллокации)
     *   — сравнение символов быстрее, чем парсинг чисел
     *
     * @param numberString строка цифр
     * @return преобразованная строка
     */
    public static String fakeBin(final String numberString) {

        // Преобразуем строку в массив символов
        final char[] chars = numberString.toCharArray();

        // Проходим по каждому символу
        for (int i = 0; i < chars.length; i++) {

            // Сравнение символов быстрее, чем преобразование в число
            chars[i] = chars[i] < '5' ? '0' : '1';
        }

        // Создаём новую строку из изменённого массива
        return new String(chars);
    }
}