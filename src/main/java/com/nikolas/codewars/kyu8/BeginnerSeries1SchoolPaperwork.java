package com.nikolas.codewars.kyu8;

/**
 * ════════════════════════════════════════════════════════════
 *  🎯 Codewars: Beginner Series #1 School Paperwork
 * ════════════════════════════════════════════════════════════
 *
 *  📋 Задача:
 *     n одноклассников, m страниц. Сколько чистых страниц нужно?
 *     Если n < 0 или m < 0 — вернуть 0.
 *
 *  📊 Примеры:
 *     5,  5   →  25
 *     -5, 5   →  0
 *     5,  -5  →  0
 *     5,  0   →  0
 *
 *  ⚡ Решение:
 *     Проверка на отрицательные + умножение.
 *     Одно сравнение, одно умножение.
 *
 *  📈 Сложность: O(1) время | O(1) память
 *
 *  🔗 https://www.codewars.com/kata/55f9b48403f6b87a7c0000bd
 *
 */
public final class BeginnerSeries1SchoolPaperwork {

    private BeginnerSeries1SchoolPaperwork() {
        throw new UnsupportedOperationException("Утилитарный класс");
    }

    /**
     * Возвращает количество чистых страниц для копирования.
     *
     *  Почему тернарный оператор:
     *     — Буквальная трансляция условия задачи: "if n < 0 or m < 0 return 0"
     *     — Math.max(n,0) * Math.max(m,0) — два вызова метода, менее читаемо
     *     — if-else — то же самое, но в двух строках
     *     — Одно сравнение + одно умножение — 2 инструкции CPU
     *
     * @param n количество одноклассников
     * @param m количество страниц
     * @return n * m если оба >= 0, иначе 0
     */
    public static int paperWork(final int n, final int m) {
        // Проверка по условию задачи + умножение
        return (n < 0 || m < 0) ? 0 : n * m;
    }
}