package com.nikolas.codewars.kyu8;

/**
 * ════════════════════════════════════════════════════════════
 *  🎯 Codewars: Total Amount of Points
 * ════════════════════════════════════════════════════════════
 *
 *  📋 Задача:
 *     Подсчитать общее количество очков футбольной команды
 *     по результатам 10 матчей.
 *
 *     Каждый матч записан строкой формата:
 *        "x:y"
 *
 *     где:
 *        x — голы нашей команды
 *        y — голы соперника
 *
 *  🏆 Правила начисления очков:
 *     x > y  →  3 очка (победа)
 *     x = y  →  1 очко (ничья)
 *     x < y  →  0 очков (поражение)
 *
 *  📊 Пример:
 *     ["3:1","2:2","0:1"]
 *
 *     3:1 → 3 очка
 *     2:2 → 1 очко
 *     0:1 → 0 очков
 *
 *     Итог: 4
 *
 *  ⚡ Решение:
 *     Используется прямое сравнение символов строки:
 *
 *        game.charAt(0)
 *        game.charAt(2)
 *
 *     По условию:
 *
 *        0 ≤ x ≤ 4
 *        0 ≤ y ≤ 4
 *
 *     Следовательно формат строки всегда фиксированный:
 *
 *        "digit:digit"
 *
 *     ASCII-коды цифр идут по порядку:
 *
 *        '0' < '1' < '2' < '3' < '4'
 *
 *     Поэтому сравнение символов эквивалентно
 *     сравнению чисел.
 *
 *  🚀 Почему это лучше других решений:
 *
 *     ✔ Нет split()
 *       split создает массив String[]
 *
 *     ✔ Нет parseInt()
 *       parseInt выполняет парсинг числа
 *
 *     ✔ Нет регулярных выражений
 *       regex компилирует Pattern и Matcher
 *
 *     ✔ Только два обращения к charAt()
 *
 *     ✔ Нулевая дополнительная аллокация памяти
 *
 *     Это максимально быстрый и минималистичный
 *     вариант для production-кода.
 *
 *  📈 Сложность:
 *
 *     Время   → O(n)
 *     Память  → O(1)
 *
 *     где n — количество матчей (в задаче всегда 10)
 *
 *  🔗 https://www.codewars.com/kata/5bb904724c47249b10000131
 *
 */
public final class TotalAmountOfPoints {

    private TotalAmountOfPoints() {
        throw new UnsupportedOperationException("Утилитарный класс");
    }

    public static int points(String[] games) {

        int total = 0;

        // Проходим по всем матчам
        for (String game : games) {

            // Берём символы голов
            char ourScore = game.charAt(0);
            char opponentScore = game.charAt(2);

            // ASCII сравнение эквивалентно сравнению чисел
            if (ourScore > opponentScore) {
                total += 3;
            } else if (ourScore == opponentScore) {
                total += 1;
            }
        }

        return total;
    }
}