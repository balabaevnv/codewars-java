package com.nikolas.codewars.kyu8;

/**
 * ════════════════════════════════════════════════════════════
 *  🎯 Codewars: Sum Arrays
 * ════════════════════════════════════════════════════════════
 *
 *  📋 Задача:
 *     Вернуть сумму всех чисел в массиве.
 *     Числа могут быть отрицательными и дробными.
 *
 *  📊 Примеры:
 *     [1, 5.2, 4, 0, -1]  →  9.2
 *     [-2.398]             →  -2.398
 *     []                   →  0
 *
 *  ⚡ Решение:
 *     For-each с аккумулятором. Один проход, ноль аллокаций.
 *
 *  📈 Сложность: O(n) время | O(1) память
 *
 *  🔗 https://www.codewars.com/kata/53dc54212259ed3d4f00071c
 *
 */
public final class SumArrays {

    private SumArrays() {
        throw new UnsupportedOperationException("Утилитарный класс");
    }

    /**
     * Возвращает сумму элементов массива.
     *
     *  Почему for-each:
     *     — 0 аллокаций — одна переменная double на стеке
     *     — DoubleStream.sum(): 3+ служебных объектов
     *     — Пустой массив: цикл не выполняется → 0.0 автоматически
     *     — Отрицательные и дробные: double арифметика корректна
     *
     * @param numbers массив дробных чисел
     * @return сумма элементов (0.0 если массив пуст)
     */
    public static double sum(final double[] numbers) {
        // Аккумулятор — единственная переменная
        double sum = 0;
        for (final double n : numbers) {
            sum += n;
        }
        return sum;
    }
}