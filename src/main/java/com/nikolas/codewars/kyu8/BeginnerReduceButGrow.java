package com.nikolas.codewars.kyu8;

/**
 * ════════════════════════════════════════════════════════════
 *  🎯 Codewars: Beginner — Reduce but Grow
 * ════════════════════════════════════════════════════════════
 *
 *  📋 Задача:
 *     Вернуть произведение всех элементов массива.
 *
 *  📊 Примеры:
 *     [1, 2, 3]           →  6
 *     [4, 1, 1, 1, 4]     →  16
 *     [2, 2, 2, 2, 2, 2]  →  64
 *
 *  ⚡ Решение:
 *     For-each с аккумулятором. Начальное значение 1
 *     (нейтральный элемент умножения).
 *
 *  📈 Сложность: O(n) время | O(1) память
 *
 *  🔗 https://www.codewars.com/kata/57f780909f7e8e3183000078
 *
 */
public final class BeginnerReduceButGrow {

    private BeginnerReduceButGrow() {
        throw new UnsupportedOperationException("Утилитарный класс");
    }

    /**
     * Возвращает произведение всех элементов массива.
     *
     *  Почему for-each:
     *     — 0 аллокаций: одна переменная int на стеке
     *     — Начальное значение 1 (нейтральный элемент умножения)
     *     — IntStream.reduce(1, *): 3+ служебных объектов
     *     — Один проход, одна инструкция imul на элемент
     *
     * @param x непустой массив целых чисел
     * @return произведение всех элементов
     */
    public static int grow(final int[] x) {
        // Аккумулятор — нейтральный элемент умножения
        int product = 1;
        for (final int n : x) {
            product *= n;
        }
        return product;
    }
}