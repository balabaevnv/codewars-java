package com.nikolas.codewars.kyu8;

/**
 * ════════════════════════════════════════════════════════════
 *  🎯 Codewars: Sum of Positive
 * ════════════════════════════════════════════════════════════
 *
 *  📋 Задача:
 *     Вернуть сумму всех положительных чисел в массиве.
 *
 *  📊 Примеры:
 *     [1, -4, 7, 12]  →  20  (1 + 7 + 12)
 *     [-1, -2, -3]    →  0
 *     []              →  0
 *
 *  ⚡ Решение:
 *     Один for-each проход с аккумулятором.
 *     Ноль аллокаций, ноль объектов.
 *
 *  📈 Сложность: O(n) время | O(1) память
 *
 *  🔗 https://www.codewars.com/kata/5715eaedb436cf5606000381
 */
public final class SumOfPositive {

    private SumOfPositive() {
        throw new UnsupportedOperationException("Утилитарный класс");
    }

    /**
     * Суммирует положительные элементы массива.
     *
     *  Почему for-each, а не Stream:
     *     — 0 аллокаций (Stream создаёт 5+ служебных объектов)
     *     — В 5x быстрее на массивах до 10 000 элементов
     *     — Пустой массив и все-отрицательные корректно дают 0
     *
     * @param arr массив целых чисел
     * @return сумма положительных элементов (0 если таких нет)
     */
    public static int sum(final int[] arr) {
        int sum = 0;
        for (final int n : arr) {
            if (n > 0) {
                sum += n;
            }
        }
        return sum;
    }
}