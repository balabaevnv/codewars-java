package com.nikolas.codewars.kyu8;

/**
 * ════════════════════════════════════════════════════════════
 *  🎯 Codewars: Simple Multiplication
 * ════════════════════════════════════════════════════════════
 *
 *  📋 Задача:
 *     Умножить число на 8, если оно чётное, или на 9, если нечётное.
 *
 *  📊 Примеры:
 *     1   →  9    (нечёт × 9)
 *     2   →  16   (чёт × 8)
 *     21  →  189  (нечёт × 9)
 *     22  →  176  (чёт × 8)
 *
 *  ⚡ Решение:
 *     Тернарный оператор: выбор множителя 8 или 9.
 *     Одно сравнение + одно умножение.
 *
 *  📈 Сложность: O(1) время | O(1) память
 *
 *  🔗 https://www.codewars.com/kata/583710ccaa6717322c000105
 *
 */
public final class SimpleMultiplication {

    private SimpleMultiplication() {
        throw new UnsupportedOperationException("Утилитарный класс");
    }

    /**
     * Умножает число на 8 (чётное) или 9 (нечётное).
     *
     *  Почему n % 2 == 0 ? 8 : 9:
     *     — Буквальная трансляция задачи: "multiply by 8 if even, by 9 otherwise"
     *     — n & 1 — побитовая проверка, менее читаемо для kyu8
     *     — 8 + (n & 1) — хак, обфускация намерения
     *     — if-else — то же, но в двух строках
     *
     * @param n целое число
     * @return n × 8 (если чётное) или n × 9 (если нечётное)
     */
    public static int simpleMultiplication(final int n) {
        // Выбор множителя по чётности
        return n * (n % 2 == 0 ? 8 : 9);
    }
}