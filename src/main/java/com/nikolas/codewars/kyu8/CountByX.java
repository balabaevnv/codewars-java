package com.nikolas.codewars.kyu8;

/**
 * ════════════════════════════════════════════════════════════
 *  🎯 Codewars: Count By X
 * ════════════════════════════════════════════════════════════
 *
 *  📋 Задача:
 *     Создать массив из первых n кратных числа x.
 *
 *  📌 Гарантия:
 *     x и n всегда положительные.
 *
 *  ⚡ Решение:
 *     Используем цикл для заполнения массива.
 *
 *  📈 Сложность: O(n) время | O(n) память
 *
 */
public final class CountByX {

    private CountByX() {
        throw new UnsupportedOperationException("Утилитарный класс");
    }

    /**
     * Возвращает массив из первых n кратных числа x.
     *
     *  Почему именно так:
     *   — 1 проход
     *   — минимум аллокаций
     *   — эффективное решение
     *
     * @param x число для которого нужно найти кратные
     * @param n количество элементов в массиве
     * @return массив первых n кратных числа x
     */
    public static int[] countBy(final int x, final int n) {

        // Инициализация массива
        int[] result = new int[n];

        // Заполняем массив кратными числа x
        for (int i = 0; i < n; i++) {
            result[i] = x * (i + 1);
        }

        return result;
    }
}