package com.nikolas.codewars.kyu8;

/**
 * ════════════════════════════════════════════════════════════
 *  🎯 Codewars: Square(n) Sum
 * ════════════════════════════════════════════════════════════
 *
 *  📋 Задача:
 *     Возвести каждое число массива в квадрат и вернуть сумму.
 *
 *  📊 Примеры:
 *     [1, 2, 2]   →  9   (1 + 4 + 4)
 *     [1, 2]      →  5   (1 + 4)
 *     [5, -3, 4]  →  50  (25 + 9 + 16)
 *     []          →  0
 *
 *  ⚡ Решение:
 *     For-each с аккумулятором: x * x вместо Math.pow().
 *     Ноль аллокаций, одна переменная на стеке.
 *
 *  📈 Сложность: O(n) время | O(1) память
 *
 *  🔗 https://www.codewars.com/kata/515e271a311df0350d00000f
 *
 */
public final class SquareNSum {

    private SquareNSum() {
        throw new UnsupportedOperationException("Утилитарный класс");
    }

    /**
     * Возвращает сумму квадратов элементов массива.
     *
     *  Почему for-each + умножение:
     *     — x * x — одна инструкция imul (целочисленная, точная)
     *     — Math.pow(x, 2) — double, каст, потеря точности, в 10-20x медленнее
     *     — Stream map + sum — 3+ объектов ради одного цикла
     *     — Пустой массив и отрицательные числа корректны без проверок
     *
     * @param n массив целых чисел
     * @return сумма квадратов элементов
     */
    public static int squareSum(final int[] n) {
        // Аккумулятор — единственная переменная
        int sum = 0;
        for (final int x : n) {
            sum += x * x;
        }
        return sum;
    }
}