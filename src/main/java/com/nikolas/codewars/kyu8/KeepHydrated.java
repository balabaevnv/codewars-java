package com.nikolas.codewars.kyu8;

/**
 * ════════════════════════════════════════════════════════════
 *  🎯 Codewars: Keep Hydrated!
 * ════════════════════════════════════════════════════════════
 *
 *  📋 Задача:
 *     Натан пьёт 0.5 литра воды за час езды на велосипеде.
 *     Вернуть количество литров, округлённое вниз.
 *
 *  📊 Примеры:
 *     3     →  1
 *     6.7   →  3
 *     11.8  →  5
 *
 *  ⚡ Решение:
 *     (int) (time / 2) — деление + отсечение дробной части.
 *     Для положительных чисел truncation = floor.
 *
 *  📈 Сложность: O(1) время | O(1) память
 *
 *  🔗 https://www.codewars.com/kata/582cb0224e56e068d800003c
 *
 */
public final class KeepHydrated {

    private KeepHydrated() {
        throw new UnsupportedOperationException("Утилитарный класс");
    }

    /**
     * Возвращает количество литров воды, округлённое вниз.
     *
     *  Почему (int) (time / 2):
     *     — Каст double → int отсекает дробную часть (truncation)
     *     — Для time >= 0: truncation = floor — результат корректен
     *     — Math.floor() — лишний вызов, результат тот же
     *     — time * 0.5 — математически идентично, но / 2 привычнее
     *
     * @param time время езды в часах (>= 0)
     * @return количество литров (округлено вниз)
     */
    public static int liters(final double time) {
        // Деление на 2 + отсечение дробной части
        return (int) (time / 2);
    }
}