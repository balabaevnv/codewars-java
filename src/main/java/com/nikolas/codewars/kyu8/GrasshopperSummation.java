package com.nikolas.codewars.kyu8;

/**
 * ════════════════════════════════════════════════════════════
 *  🎯 Codewars: Grasshopper — Summation
 * ════════════════════════════════════════════════════════════
 *
 *  📋 Задача:
 *     Найти сумму всех чисел от 1 до n включительно.
 *
 *  📊 Примеры:
 *     1  →  1   (1)
 *     2  →  3   (1 + 2)
 *     8  →  36  (1 + 2 + 3 + 4 + 5 + 6 + 7 + 8)
 *
 *  ⚡ Решение:
 *     Формула Гаусса: n * (n + 1) / 2.
 *     Три машинные инструкции вместо n итераций цикла.
 *
 *  📈 Сложность: O(1) время | O(1) память
 *
 *  🔗 https://www.codewars.com/kata/55d24f55d7dd296eb9000030
 *
 */
public final class GrasshopperSummation {

    private GrasshopperSummation() {
        throw new UnsupportedOperationException("Утилитарный класс");
    }

    /**
     * Возвращает сумму чисел от 1 до n включительно.
     *
     *  Почему формула Гаусса:
     *     — O(1) вместо O(n) цикла
     *     — 3 инструкции: imul + iadd + idiv
     *     — n * (n + 1) всегда чётное — деление на 2 точное
     *     — Цикл: n итераций + n сложений
     *     — Stream: 3+ объектов + n итераций
     *     — Рекурсия: n стековых фреймов → StackOverflow при больших n
     *
     * @param n положительное целое число (n > 0)
     * @return сумма 1 + 2 + ... + n
     */
    public static int summation(final int n) {
        // Формула Гаусса — константное время
        return n * (n + 1) / 2;
    }
}