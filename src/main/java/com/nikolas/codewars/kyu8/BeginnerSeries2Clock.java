package com.nikolas.codewars.kyu8;

/**
 * ════════════════════════════════════════════════════════════
 *  🎯 Codewars: Beginner Series #2 Clock
 * ════════════════════════════════════════════════════════════
 *
 *  📋 Задача:
 *     Часы показывают h часов, m минут, s секунд после полуночи.
 *     Вернуть время в миллисекундах.
 *
 *  📊 Примеры:
 *     0, 1, 1   →  61000
 *     1, 0, 0   →  3600000
 *     0, 0, 0   →  0
 *
 *  ⚡ Решение:
 *     Формула: (h×3600 + m×60 + s) × 1000.
 *     Три умножения, два сложения.
 *
 *  📈 Сложность: O(1) время | O(1) память
 *
 *  🔗 https://www.codewars.com/kata/55f9bca8ecaa9eac7100004a
 *
 */
public final class BeginnerSeries2Clock {

    private BeginnerSeries2Clock() {
        throw new UnsupportedOperationException("Утилитарный класс");
    }

    /**
     * Возвращает время после полуночи в миллисекундах.
     *
     *  Почему прямая формула:
     *     — 3600, 60, 1000 — общеизвестные константы
     *     — TimeUnit: три вызова метода + long арифметика + каст
     *     — h*3600000: magic number, нужно считать в уме
     *     — Максимум 86_399_000 — далеко от Integer.MAX_VALUE
     *
     * @param h часы (0-23)
     * @param m минуты (0-59)
     * @param s секунды (0-59)
     * @return миллисекунды с полуночи
     */
    public static int past(final int h, final int m, final int s) {
        // Часы→секунды + минуты→секунды + секунды → всё в миллисекунды
        return ((h * 3600) + (m * 60) + s) * 1000;
    }
}